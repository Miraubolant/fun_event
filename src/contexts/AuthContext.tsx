import React, { createContext, useContext, useState, ReactNode } from 'react';
import { supabase } from '../lib/supabase';
import { User } from '../types';

interface AuthContextType {
  user: User | null;
  loading: boolean;
  login: (email: string, password: string) => Promise<boolean>;
  logout: () => void;
  isAdmin: boolean;
}

const AuthContext = createContext<AuthContextType | undefined>(undefined);

export const useAuth = () => {
  const context = useContext(AuthContext);
  if (context === undefined) {
    throw new Error('useAuth must be used within an AuthProvider');
  }
  return context;
};

interface AuthProviderProps {
  children: ReactNode;
}

export const AuthProvider: React.FC<AuthProviderProps> = ({ children }) => {
  const [user, setUser] = useState<User | null>(null);
  const [loading, setLoading] = useState(true);

  const checkAdminStatus = async (userId: string) => {
    try {
      console.log('üîç V√©rification du statut admin pour:', userId);
      
      // V√©rifier d'abord si l'utilisateur est connect√©
      const { data: { session } } = await supabase.auth.getSession();
      if (!session) {
        console.log('‚ùå Pas de session active');
        return false;
      }
      
      const { data: adminUser, error: adminError } = await supabase
        .from('admin_users')
        .select('*')
        .eq('id', userId)
        .single();

      console.log('üîç R√©sultat v√©rification admin:', { 
        adminUser, 
        adminError: adminError?.message,
        userId 
      });
      
      if (adminUser) {
        console.log('‚úÖ Utilisateur admin confirm√©:', adminUser);
        setUser({
          id: userId,
          email: adminUser.email || '',
          role: 'admin'
        });
        return true;
      } else if (adminError && adminError.code !== 'PGRST116') {
        console.error('‚ùå Erreur lors de la v√©rification admin:', adminError.message);
        return false;
      } else {
        console.log('‚ùå Utilisateur non-admin ou non trouv√©');
        return false;
      }
    } catch (error) {
      console.error('‚ùå Erreur lors de checkAdminStatus:', error);
      return false;
    }
  };

  // Fonction pour cr√©er un utilisateur admin
  const createAdminUser = async (userId: string, email: string) => {
    try {
      console.log('üîß Tentative de cr√©ation de l\'enregistrement admin...');
      const { data: insertData, error: insertError } = await supabase
        .from('admin_users')
        .insert({
          id: userId,
          email: email,
          role: 'admin'
        })
        .select()
        .single();
      
      if (insertData && !insertError) {
        console.log('‚úÖ Enregistrement admin cr√©√© avec succ√®s:', insertData);
        setUser({
          id: userId,
          email: email,
          role: 'admin'
        });
        return true;
      } else {
        console.error('‚ùå Erreur cr√©ation admin:', insertError?.message);
        return false;
      }
    } catch (error) {
      console.error('‚ùå Erreur lors de createAdminUser:', error);
      return false;
    }
  };

  // V√©rifier l'√©tat d'authentification au chargement
  React.useEffect(() => {
    const checkAuth = async () => {
      try {
        console.log('üîç V√©rification de l\'authentification...');
        const { data: { session } } = await supabase.auth.getSession();
        console.log('üìã Session actuelle:', session?.user?.id || 'Aucune session');
        
        if (session?.user) {
          console.log('üë§ Utilisateur connect√©, v√©rification du statut admin...');
          // V√©rifier si l'utilisateur est admin
          const isAdmin = await checkAdminStatus(session.user.id);
          console.log('üéØ R√©sultat final checkAdminStatus:', isAdmin);
        } else {
          console.log('‚ùå Aucune session active');
        }
      } catch (error) {
        console.error('‚ùå Erreur lors de la v√©rification de l\'authentification:', error);
      } finally {
        setLoading(false);
      }
    };

    checkAuth();

    // √âcouter les changements d'authentification
    const { data: { subscription } } = supabase.auth.onAuthStateChange(async (event, session) => {
      console.log('üîÑ Changement d\'√©tat auth:', event, session?.user?.id || 'Aucune session');
      
      if (event === 'SIGNED_IN' && session?.user) {
        console.log('‚úÖ Connexion d√©tect√©e, v√©rification admin...');
        // V√©rifier si l'utilisateur est admin
        const isAdmin = await checkAdminStatus(session.user.id);
        console.log('üéØ R√©sultat final onAuthStateChange:', isAdmin);
      } else if (event === 'SIGNED_OUT') {
        console.log('üö™ D√©connexion d√©tect√©e');
        setUser(null);
      }
    });

    return () => subscription.unsubscribe();
  }, []);

  const login = async (email: string, password: string): Promise<boolean> => {
    try {
      console.log('üîê Tentative de connexion pour:', email);
      const { data, error } = await supabase.auth.signInWithPassword({
        email,
        password
      });

      if (error) {
        console.error('‚ùå Erreur de connexion Supabase:', error.message);
        return false;
      }

      if (data.user) {
        console.log('‚úÖ Utilisateur connect√©:', data.user.id);
        console.log('üìß Email:', data.user.email);
        
        console.log('üîç V√©rification du statut admin...');
        
        // V√©rifier le statut admin
        const isAdmin = await checkAdminStatus(data.user.id);
        
        if (isAdmin) {
          console.log('üéâ Connexion admin r√©ussie !');
          return true;
        }
        
        // Si pas admin mais email autoris√©, cr√©er le compte admin
        if (data.user.email === 'victor@mirault.com') {
          console.log('üìß Email autoris√©, cr√©ation du compte admin...');
          const created = await createAdminUser(data.user.id, data.user.email);
          if (created) {
            console.log('üéâ Compte admin cr√©√© et connexion r√©ussie !');
            return true;
          }
        } else {
          console.log('‚ùå Email non autoris√© pour admin');
        }
        
        // D√©connecter si pas admin
        await supabase.auth.signOut();
        return false;
      }

      return false;
    } catch (error) {
      console.error('‚ùå Erreur lors de la connexion:', error);
      return false;
    }
  };

  const logout = async () => {
    try {
      console.log('üö™ D√©connexion...');
      await supabase.auth.signOut();
      setUser(null);
      console.log('‚úÖ D√©connexion r√©ussie');
    } catch (error) {
      console.error('‚ùå Erreur lors de la d√©connexion:', error);
    }
  };

  const isAdmin = user?.role === 'admin';

  return (
    <AuthContext.Provider value={{ user, loading, login, logout, isAdmin }}>
      {children}
    </AuthContext.Provider>
  );
};