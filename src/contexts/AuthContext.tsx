import React, { createContext, useContext, useState, ReactNode } from 'react';
import { supabase } from '../lib/supabase';
import { User } from '../types';

interface AuthContextType {
  user: User | null;
  loading: boolean;
  login: (email: string, password: string) => Promise<boolean>;
  logout: () => void;
  isAdmin: boolean;
}

const AuthContext = createContext<AuthContextType | undefined>(undefined);

export const useAuth = () => {
  const context = useContext(AuthContext);
  if (context === undefined) {
    throw new Error('useAuth must be used within an AuthProvider');
  }
  return context;
};

interface AuthProviderProps {
  children: ReactNode;
}

export const AuthProvider: React.FC<AuthProviderProps> = ({ children }) => {
  const [user, setUser] = useState<User | null>(null);
  const [loading, setLoading] = useState(true);

  // V√©rifier l'√©tat d'authentification au chargement
  React.useEffect(() => {
    const checkAuth = async () => {
      try {
        console.log('üîç V√©rification de l\'authentification...');
        const { data: { session } } = await supabase.auth.getSession();
        console.log('üìã Session actuelle:', session?.user?.id || 'Aucune session');
        
        if (session?.user) {
          console.log('üë§ Utilisateur connect√©, v√©rification du statut admin...');
          // V√©rifier si l'utilisateur est admin
          const { data: adminUser, error: adminError } = await supabase
            .from('admin_users')
            .select('*')
            .eq('id', session.user.id)
            .single();

          console.log('üîç Requ√™te admin_users:', { adminUser, adminError });
          
          if (adminUser) {
            console.log('‚úÖ Utilisateur admin confirm√©');
            setUser({
              id: session.user.id,
              email: session.user.email || '',
              role: 'admin'
            });
          } else {
            console.log('‚ùå Utilisateur non-admin ou erreur:', adminError?.message);
          }
        } else {
          console.log('‚ùå Aucune session active');
        }
      } catch (error) {
        console.error('‚ùå Erreur lors de la v√©rification de l\'authentification:', error);
      } finally {
        setLoading(false);
      }
    };

    checkAuth();

    // √âcouter les changements d'authentification
    const { data: { subscription } } = supabase.auth.onAuthStateChange(async (event, session) => {
      console.log('üîÑ Changement d\'√©tat auth:', event, session?.user?.id || 'Aucune session');
      
      if (event === 'SIGNED_IN' && session?.user) {
        console.log('‚úÖ Connexion d√©tect√©e, v√©rification admin...');
        // V√©rifier si l'utilisateur est admin
        const { data: adminUser, error: adminError } = await supabase
          .from('admin_users')
          .select('*')
          .eq('id', session.user.id)
          .single();

        console.log('üîç V√©rification admin apr√®s connexion:', { adminUser, adminError });
        
        if (adminUser) {
          console.log('‚úÖ Admin confirm√© apr√®s connexion');
          setUser({
            id: session.user.id,
            email: session.user.email || '',
            role: 'admin'
          });
        } else {
          console.log('‚ùå Non-admin apr√®s connexion');
          setUser(null);
        }
      } else if (event === 'SIGNED_OUT') {
        console.log('üö™ D√©connexion d√©tect√©e');
        setUser(null);
      }
    });

    return () => subscription.unsubscribe();
  }, []);

  const login = async (email: string, password: string): Promise<boolean> => {
    try {
      console.log('Tentative de connexion pour:', email);
      const { data, error } = await supabase.auth.signInWithPassword({
        email,
        password
      });

      if (error) {
        console.error('Erreur de connexion Supabase:', error.message);
        return false;
      }

      if (data.user) {
        console.log('Utilisateur connect√©:', data.user.id);
        
        // Attendre un peu pour que la session soit bien √©tablie
        await new Promise(resolve => setTimeout(resolve, 500));
        
        // V√©rifier si l'utilisateur est admin
        const { data: adminUser, error: adminError } = await supabase
          .from('admin_users')
          .select('*')
          .eq('id', data.user.id)
          .single();

        console.log('üîç V√©rification admin lors du login:', { adminUser, adminError });

        if (adminUser) {
          console.log('‚úÖ Utilisateur admin confirm√©:', adminUser);
          setUser({
            id: data.user.id,
            email: data.user.email || '',
            role: 'admin'
          });
          return true;
        } else {
          console.log('‚ùå Utilisateur non-admin ou erreur admin:', adminError?.message);
          // D√©connecter si pas admin
          await supabase.auth.signOut();
          return false;
        }
      }

      return false;
    } catch (error) {
      console.error('‚ùå Erreur lors de la connexion:', error);
      return false;
    }
  };

  const logout = async () => {
    try {
      console.log('üö™ D√©connexion...');
      await supabase.auth.signOut();
      setUser(null);
      console.log('‚úÖ D√©connexion r√©ussie');
    } catch (error) {
      console.error('‚ùå Erreur lors de la d√©connexion:', error);
    }
  };

  const isAdmin = user?.role === 'admin';

  return (
    <AuthContext.Provider value={{ user, loading, login, logout, isAdmin }}>
      {children}
    </AuthContext.Provider>
  );
};